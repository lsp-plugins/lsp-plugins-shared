<p><b>LSP (Linux Studio Plugins)</b> is a collection of open-source plugins compatible with following plugin formats:</p>
<ul>
	<li><b>CLAP</b> - set of plugins for <a href="https://github.com/free-audio/clap">Clever Audio Plugins API</a></li>
	<li><b>JACK</b> - Standalone versions for <a href="https://jackaudio.org/">JACK Audio connection Kit</a> with UI</li>
	<li><b>LADSPA</b> - set of plugins for <a href="https://en.wikipedia.org/wiki/LADSPA">Linux Audio Developer's Simple Plugin API</a></li>
	<li><b>LV2</b> - set of plugins and UIs for <a href="https://lv2plug.in/">Linux Audio Developer's Simple Plugin API (LADSPA) version 2</a></li>
	<li><b>VST2 / LinuxVST</b> - set of plugins for <a href="https://www.steinberg.net/">Steinberg's VST 2.4</a> plugin format</li>
	<li><b>VST3</b> - set of plugins and for <a href="https://www.steinberg.net/">Steinberg's VST 3</a> plugin format</li>
</ul>

<p>The basic idea is to fill the lack of good and useful plugins under the GNU/Linux platform.</p>

<p>After some contributions to other open source projects the decision was made to implement separate and independent plugin distribution.</p>

<p>Currently project is developed and maintained by LSP Project Team with the main founder and developer at the head &ndash; Vladimir Sadovnikov.</p>

<p>The official site for the project is located here: <a href="https://lsp-plug.in/">https://lsp-plug.in/</a></p>
